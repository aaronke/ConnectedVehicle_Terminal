package cst.aaronke.connectedvehicle_terminal.utilities;

public class MsgObject {
	private String bsm_count;
	private String bsm_tembID;
	private String bsm_latitude;
	private String bsm_longitude;
	private String bsm_elevation;
	private String bsm_speed;
	public String getBsm_count() {
		return bsm_count;
	}
	public void setBsm_count(String bsm_count) {
		this.bsm_count = bsm_count;
	}
	public String getBsm_tembID() {
		return bsm_tembID;
	}
	public void setBsm_tembID(String bsm_tembID) {
		this.bsm_tembID = bsm_tembID;
	}
	public String getBsm_latitude() {
		return bsm_latitude;
	}
	public void setBsm_latitude(String bsm_latitude) {
		this.bsm_latitude = bsm_latitude;
	}
	public String getBsm_longitude() {
		return bsm_longitude;
	}
	public void setBsm_longitude(String bsm_longitude) {
		this.bsm_longitude = bsm_longitude;
	}
	public String getBsm_elevation() {
		return bsm_elevation;
	}
	public void setBsm_elevation(String bsm_elevation) {
		this.bsm_elevation = bsm_elevation;
	}
	public String getBsm_speed() {
		return bsm_speed;
	}
	public void setBsm_speed(String bsm_speed) {
		this.bsm_speed = bsm_speed;
	}
	
	
}
